fun main() {
var isProfessor: Boolean = false
    isProfessor = !(isProfessor)
    print("You are a professor?: $isProfessor" + "\n")

val nota1: Double = 7.3
val nota2: Double = 7.7
val media: Double = (nota1 + nota2) / 2
print("You division is: $media" + "\n")

var personOne: String = "Luis"
var personTwo: String = "João"
print("Is the person name $personOne the same as $personTwo? ${personOne == personTwo}")
}

